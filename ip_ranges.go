// IPRangeProvider checks if the given IP address belongs to a known cloud provider.
// Returns ProviderUnknown if not found.
func IPRangeProvider(addr string) CloudProvider {
	once.Do(initRanges)

	if addr == "" {
		return ProviderUnknown
	}

	// Handle addr with port (e.g., "192.168.1.1:8080")
	host, _, err := net.SplitHostPort(addr)
	if err != nil {
		host = addr
	}

	ip := net.ParseIP(host)
	if ip == nil {
		return ProviderUnknown
	}

	for _, r := range cloudRanges {
		if r.network.Contains(ip) {
			return r.provider
		}
	}

	return ProviderUnknown
}

// IsCloudProvider returns true if the IP belongs to any known cloud provider.
func IsCloudProvider(addr string) bool {
	return IPRangeProvider(addr) != ProviderUnknown
}
