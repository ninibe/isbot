func (p CloudProvider) String() string {
	switch p {
	case ProviderAWS:
		return "AWS"
	case ProviderDigitalOcean:
		return "DigitalOcean"
	case ProviderGoogleCloud:
		return "GoogleCloud"
	case ProviderHetzner:
		return "Hetzner"
	case ProviderAzure:
		return "Azure"
	case ProviderOVH:
		return "OVH"
	case ProviderOracleCloud:
		return "OracleCloud"
	case ProviderIBMCloud:
		return "IBMCloud"
	default:
		return "Unknown"
	}
}

type ipRange struct {
	provider CloudProvider
	network  *net.IPNet
}

var (
	cloudRanges []ipRange
	once               sync.Once
)

func mustParseCIDR(cidr string, provider CloudProvider) ipRange {
	_, network, err := net.ParseCIDR(cidr)
	if err != nil {
		panic("invalid CIDR: " + cidr + ": " + err.Error())
	}
	return ipRange{provider: provider, network: network}
}

